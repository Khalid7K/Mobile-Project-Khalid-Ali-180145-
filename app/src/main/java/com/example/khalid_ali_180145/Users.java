package com.example.khalid_ali_180145;

public class Users {
    private String name;
    private String email;
    private String id;

    public Users() {
    }

    public Users(String name, String mail, String id) {
        this.name = name;
        this.email = email;
        this.id = id;
    }

    public String getname() {
        return name;
    }

    public String getemail() {

        return email;
    }

    public String getid() {

        return id;
    }

}

